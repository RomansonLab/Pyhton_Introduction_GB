# Задача 34:  Винни-Пух попросил Вас посмотреть, есть ли в его стихах ритм.
# Поскольку разобраться в его кричалках не настолько просто, насколько легко он их придумывает,
# Вам стоит написать программу. Винни-Пух считает, что ритм есть, если число слогов (т.е. число гласных букв)
# в каждой фразе стихотворения одинаковое. Фраза может состоять из одного слова, если во фразе несколько слов,
# то они разделяются дефисами. Фразы отделяются друг от друга пробелами. Стихотворение  Винни-Пух вбивает в программу
# с клавиатуры. В ответе напишите “Парам пам-пам”, если с ритмом все в порядке и “Пам парам”,
# если с ритмом все не в порядке
#
# *Пример:*
#
# **Ввод:** пара-ра-рам рам-пам-папам па-ра-па-да
#     **Вывод:** Парам пам-пам

def count_vowels(word):
    vowels = "аеёиоуыэюя"  # гласные буквы
    count = 0
    for letter in word:
        if letter in vowels:
            count += 1
    return count

def check_rhythm(poem):
    lines = poem.split()  # разбиваем стихотворение на фразы
    vowel_counts = []  # список для хранения числа гласных в каждой фразе

    for line in lines:
        words = line.split("-")  # разбиваем фразу по дефисам
        total_vowels = sum(count_vowels(word) for word in words)  # общее число гласных во фразе
        vowel_counts.append(total_vowels)

    if same_by(lambda x: x, vowel_counts):
        return "Парам пам-пам"
    else:
        return "Пам парам"

def same_by(characteristic, objects):
    return len(set(map(characteristic, objects))) < 2

poem = input("Введите стихотворение: ")
result = check_rhythm(poem)
print(result)